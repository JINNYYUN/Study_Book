CREATE TABLE BBS(
	SEQ NUMBER(8) PRIMARY KEY,
	EMAIL VARCHAR2(50) NOT NULL,
	
	REF NUMBER(8) NOT NULL,
	STEP NUMBER(8) NOT NULL,
	DEPTH NUMBER(8) NOT NULL,
	
	TITLE VARCHAR2(200) NOT NULL,
	CONTENT VARCHAR2(4000) NOT NULL,
	WDATE DATE NOT NULL,
	
	DEL NUMBER(1) NOT NULL,
	READCOUNT NUMBER(8) NOT NULL
);

CREATE SEQUENCE SEQ_BBS
START WITH 1
INCREMENT BY 1;

ALTER TABLE BBS
ADD CONSTRAINT FK_BBS_ID FOREIGN KEY(EMAIL)
REFERENCES MEMBER(EMAIL);

SELECT *
FROM BBS

SELECT SEQ, EMAIL, REF, STEP, DEPTH, TITLE, CONTENT, WDATE, DEL, READCOUNT
FROM 
	(SELECT ROW_NUMBER()OVER(ORDER BY REF DESC, STEP ASC) AS RNUM,
		SEQ, EMAIL, REF, STEP, DEPTH, TITLE, CONTENT, WDATE, DEL, READCOUNT
	FROM BBS	
	ORDER BY REF DESC, STEP ASC)
WHERE RNUM >= 1 AND RNUM <= 10 <-- 이 경우 1부터 10까지 가져 옴-->

